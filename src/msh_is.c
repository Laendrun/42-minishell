#include "minishell.h"

int	msh_isspace(int c)
{
	return (c == 9 || c == 10 || c == 11 || c == 12 || c == 13 || c == 32);
}

int	msh_isoperator(int c)
{
	return (c == 38 || c == 60 || c == 62 || c == 124);
}

int	msh_isspec(int c)
{
	return (c == 40 || c == 41 || c == 123 || c == 125 || c == 91 || c == 93);
}
